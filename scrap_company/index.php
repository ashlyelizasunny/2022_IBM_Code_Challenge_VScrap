          <h2>Frequently Asked Questions</h2>
          <p>The scrappage policy launched on August 13th 2021,is a
government founded programme to replace old vehicles to
new and modern ones.
.</p>
        </div>

        <div class="accordion-list">
          <ul>
            <li data-aos="fade-up">
              <i class="bx bx-help-circle icon-help"></i> <a data-toggle="collapse" class="collapse" href="#accordion-list-1">WHAT EXACTLY IS VEHICLE SCRAPPAGE POLICY? <i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="accordion-list-1" class="collapse show" data-parent=".accordion-list">
                <p>
               Much like the western countries, the scrappage policy comes into effect when a vehicle’s registration is complete. In general, a passenger vehicle has a life of 15 years and a commercial vehicle has a life of 10 years, after which they become obsolete and also starts polluting the environment at a greater intensity than they would have been doing earlier. In western countries, these old vehicles are sent to scrapyards where they are dismantled and the steel used for making the body is crushed and recycled again. In India though, there was no such policy. Most of the vehicles either are on run currently polluting the environment or are lying at road sides.  </p>
              </div>
            </li>

            <li data-aos="fade-up" data-aos-delay="100">
              <i class="bx bx-help-circle icon-help"></i> <a data-toggle="collapse" href="#accordion-list-2" class="collapsed">WHAT DOES THE POLICY AIM TO ACHIEVE? <i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="accordion-list-2" class="collapse" data-parent=".accordion-list">
                <p>
             As explained above, a vehicle should be scrapped at the end of its lifecycle so that they are stopped running on roads that will reduce air pollution. Also, getting off older vehicles will generate space for new vehicles, which will boost the sales in the otherwise battered and bruised Indian auto industry. Nitin Gadkari explained that once the registration of the vehicles are over, they will have to go mandatory fitness test. However, they can also avail scrapping option.    </p>
              </div>
            </li>

            <li data-aos="fade-up" data-aos-delay="200">
              <i class="bx bx-help-circle icon-help"></i> <a data-toggle="collapse" href="#accordion-list-3" class="collapsed">WILL ALL VEHICLES OVER AND ABOVE TIME LIMIT WILL BE SCRAPPED? <i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="accordion-list-3" class="collapse" data-parent=".accordion-list">
                <p>
               No, not all vehicles will be scrapped as this is a voluntary scheme and not a mandatory one. However, there’s a catch here. All vehicles over and above the time limit will have to undergo mandatory fitness test. If a vehicles fails fitness test, they will not get renewal certificate and won’t be able to run on road. However, if they pass fitness test, they will have to undergo fitness test after every 5 years again to show their road worthiness.  </p>
              </div>
            </li>

            <li data-aos="fade-up" data-aos-delay="300">
              <i class="bx bx-help-circle icon-help"></i> <a data-toggle="collapse" href="#accordion-list-4" class="collapsed">WHAT BENEFIT WILL I GET FROM SCRAPPING MY VEHICLE? <i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
              <div id="accordion-list-4" class="collapse" data-parent=".accordion-list">
                <p>
              Nitin Gadkari has given following financial benefits one can get from the policy-
<br>
1) A scrap value of the ex-showroom price of vehicle ranging from 4-6% will be given to the owner if they choose to scrap the vehicle
<br>
2) A rebate of upto 25% will be given in Road Tax
<br>
3) Vehicle manufacturers will be advised to give 5% discount on new vehicles against a scrapping certificate
<br>
4) No registration fees for vehicles.   </p>
